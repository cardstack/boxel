#!/usr/bin/env bash
# Lint the observability package: shell scripts, JSON manifests, YAML, plus
# a regression check on the `grafana_secret` placeholder in dashboard JSON.
#
# What's checked:
#   1. shellcheck — every *.sh under scripts/ (matches the package's content;
#      no JS/TS to ESLint here).
#   2. jq empty   — every .json under grafanactl/resources/ and provisioning/
#      parses as JSON. Cheap and catches malformed dashboard / datasource /
#      alert manifests.
#   3. Manifest shape — each grafanactl/resources/{dashboards,folders}/...json
#      has top-level apiVersion + kind + metadata.name. A self-contained
#      schema sanity check (doesn't need a live Grafana, unlike `grafanactl
#      resources validate` which contacts the server). Catches the "I forgot
#      to wrap this dashboard JSON in App Platform metadata" class of mistake.
#   4. Secret-shape scan — every constant template variable named
#      `grafana_secret` must hold the placeholder REPLACE_AT_APPLY_TIME.
#      The real value gets substituted at apply time. If a UUID-shaped
#      secret slips into a committed dashboard, the lint fails before the
#      file lands on main. Narrowed to templating.list[] (where the
#      `grafana_secret` constant lives) so this doesn't trip on UUID-shaped
#      values in unrelated `query` fields elsewhere in the dashboard JSON.
#   5. prettier --check — YAML files only. Mirrors the rest of the repo's
#      YAML formatting policy. JSON is excluded on purpose: dashboards are
#      regenerated by `grafanactl resources pull` (via scripts/pull.sh),
#      and reformatting them would cause the next pull to fight prettier.
#   6. (optional) `grafanactl resources validate` — only if
#      GRAFANACTL_VALIDATE_ENV is set (with GRAFANA_TOKEN sourced). The
#      observability-apply-{staging,production}.yml and observability-diff.yml
#      workflows opt in: they already source GRAFANA_TOKEN from SSM before
#      lint runs, so this gives manifest-vs-live-server validation at PR /
#      merge time. Plain `pnpm lint` runs in ci-lint.yaml have no token and
#      skip this step (the other 5 checks still cover the offline cases in
#      the acceptance criteria).
#
# This script is invoked two ways:
#   - via package.json: `pnpm --filter @cardstack/observability lint`
#     → ci-lint.yaml runs this on every PR, no token, skips step 6.
#   - directly via `bash ./scripts/lint.sh` from the apply/diff workflows
#     after they've sourced GRAFANA_TOKEN from SSM, which enables step 6
#     when GRAFANACTL_VALIDATE_ENV is also set.
set -eo pipefail

cd "$(dirname "$0")/.."
PKG_DIR="$(pwd)"
REPO_ROOT="$(cd ../.. && pwd)"

fail=0
section() { printf '\n=== %s ===\n' "$*"; }
err() { echo "  ✗ $*" >&2; fail=1; }
ok()  { echo "  ✓ $*"; }

# 1. shellcheck
section "shellcheck"
if ! command -v shellcheck >/dev/null 2>&1; then
  err "shellcheck not installed (brew install shellcheck / apt-get install -y shellcheck)"
else
  # SC1091 is suppressed: the `# shellcheck source=...` directives in each
  # script document the sourced file's location, but shellcheck without -x
  # still emits an info-level "Not following" line. Excluding the rule
  # silences that noise without dropping severity for actual issues like
  # SC2086 (unquoted variable) — that one we want to fail on.
  if shellcheck --exclude=SC1091 scripts/*.sh; then
    ok "all shell scripts pass"
  else
    fail=1
  fi
fi

# 2. jq empty (JSON syntax)
section "jq empty (JSON syntax)"
if ! command -v jq >/dev/null 2>&1; then
  err "jq not installed (brew install jq / apt-get install -y jq)"
else
  json_count=0
  while IFS= read -r f; do
    json_count=$((json_count + 1))
    if ! jq empty "$f" 2>/dev/null; then
      err "$f: malformed JSON"
      jq empty "$f" 2>&1 | sed 's/^/    /' >&2 || true
    fi
  done < <(find grafanactl/resources provisioning -type f -name '*.json')
  ok "$json_count JSON files parse"
fi

# 3. Manifest shape — every dashboards/**/*.json and folders/*.json has
#    apiVersion + kind + metadata.name (App Platform required fields).
#    Use `find` rather than bash's globstar to keep this working on
#    stock macOS bash 3.2 too.
section "manifest shape (apiVersion + kind + metadata.name)"
manifest_count=0
while IFS= read -r f; do
  manifest_count=$((manifest_count + 1))
  missing="$(jq -r '
    [
      (if (.apiVersion // "" | length) > 0 then empty else "apiVersion" end),
      (if (.kind // "" | length) > 0 then empty else "kind" end),
      (if (.metadata.name // "" | length) > 0 then empty else "metadata.name" end)
    ] | join(", ")
  ' "$f" 2>/dev/null || true)"
  if [[ -n "$missing" ]]; then
    err "$f: missing required field(s): $missing"
  fi
done < <(find grafanactl/resources/dashboards grafanactl/resources/folders -type f -name '*.json' 2>/dev/null)
[[ "$fail" -eq 0 ]] && ok "$manifest_count manifests have apiVersion/kind/metadata.name"

# 4. Secret-shape scan — `grafana_secret` template variable must always be
#    REPLACE_AT_APPLY_TIME. The real value gets substituted at apply time.
#    Failing here means a UUID-shaped secret leaked into a committed dashboard.
section "secret-shape scan (templating.list[].grafana_secret)"
dashboard_count=0
while IFS= read -r f; do
  dashboard_count=$((dashboard_count + 1))
  # `[ ... ] | all(...)` is true when the array is empty (no grafana_secret
  # variable in this dashboard) or every entry equals the placeholder.
  if ! jq -e '
    [ .spec.templating.list[]?
      | select(.type == "constant" and .name == "grafana_secret")
      | .query
    ] | all(. == "REPLACE_AT_APPLY_TIME")
  ' "$f" >/dev/null 2>&1; then
    err "$f: a 'grafana_secret' constant template variable is not REPLACE_AT_APPLY_TIME"
    err "  — replace the real value with the REPLACE_AT_APPLY_TIME placeholder before committing."
  fi
done < <(find grafanactl/resources/dashboards -type f -name '*.json' 2>/dev/null)
[[ "$fail" -eq 0 ]] && ok "$dashboard_count dashboards: grafana_secret == REPLACE_AT_APPLY_TIME (or absent)"

# 5. prettier --check (YAML only).
#    Run from REPO_ROOT so prettier picks up the root .prettierrc.js (which
#    sets singleQuote: false for YAML). Patterns are package-scoped.
section "prettier --check (YAML)"
if [[ "${PRETTIER_SKIP:-}" == "1" ]]; then
  # Honored before the pnpm-availability check so the diff/apply workflows
  # — which intentionally don't run `pnpm install` to stay fast — can opt
  # out without tripping the missing-pnpm error.
  echo "  PRETTIER_SKIP=1 — skipping prettier check"
elif ! command -v pnpm >/dev/null 2>&1; then
  err "pnpm not installed — required for prettier (skip with PRETTIER_SKIP=1 if running outside the workspace)"
else
  ( cd "$REPO_ROOT" && \
    pnpm exec prettier --check \
      'packages/observability/**/*.yaml' \
      'packages/observability/**/*.yml' ) \
  || fail=1
fi

# 6. (optional) grafanactl resources validate.
#    Skipped unless GRAFANACTL_VALIDATE_ENV names a context. The diff /
#    apply CI workflows set this and have GRAFANA_TOKEN in their env, so
#    they get manifest-vs-live-server validation. Local `pnpm lint` runs
#    don't, and that's fine — the offline checks above cover the
#    acceptance criteria.
section "grafanactl resources validate"
if [[ -z "${GRAFANACTL_VALIDATE_ENV:-}" ]]; then
  echo "  skipped — set GRAFANACTL_VALIDATE_ENV=local|staging|production (with GRAFANA_TOKEN) to enable."
elif ! command -v grafanactl >/dev/null 2>&1; then
  err "grafanactl not installed but GRAFANACTL_VALIDATE_ENV is set"
elif [[ "${GRAFANACTL_VALIDATE_ENV}" != "local" && -z "${GRAFANA_TOKEN:-}" ]]; then
  err "GRAFANACTL_VALIDATE_ENV=${GRAFANACTL_VALIDATE_ENV} requires GRAFANA_TOKEN to be set"
else
  cfg="$("${PKG_DIR}/scripts/render-config.sh" "${GRAFANACTL_VALIDATE_ENV}")"
  trap 'rm -f "$cfg"' EXIT
  if grafanactl --config "$cfg" --context "${GRAFANACTL_VALIDATE_ENV}" \
       resources validate --path ./grafanactl/resources; then
    ok "manifests validate against ${GRAFANACTL_VALIDATE_ENV} Grafana"
  else
    fail=1
  fi
fi

echo ""
if [[ "$fail" -eq 0 ]]; then
  echo "lint OK"
else
  echo "lint failed" >&2
  exit 1
fi
